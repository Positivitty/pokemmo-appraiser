export function getPokemonById(id: number): PokemonData | undefined {
  return POKEMON_DATA.find(p => p.id === id);
}

export function searchPokemon(query: string): PokemonData[] {
  const q = query.toLowerCase().trim();
  if (!q) return POKEMON_DATA;
  return POKEMON_DATA.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.id.toString() === q ||
    p.types.some(t => t.includes(q))
  );
}
